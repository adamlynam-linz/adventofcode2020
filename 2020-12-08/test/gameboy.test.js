const gameboy = require('../src/gameboy');
const noloop = require('../src/gameboynoloop');

test('test input part 1', () => {
  fs = require('fs');
  fs.readFile('test-input', 'utf8', function (err,data) {
    if (err) throw err;
    expect(gameboy(data)).toBe(5);
  });
});

